//! MP5 Alpha smoke: ORIGAMI FLAC → MP5-L v3 export + validation.
//!   cargo run --release -p mp5-codec --features bench_tools --bin alpha_origami_smoke

use mp5_codec::mp5l;
use std::collections::HashMap;
use std::fs;
use std::path::{Path, PathBuf};
use std::time::Instant;

struct Pcm {
    samples: Vec<i16>,
    channels: u8,
    sample_rate: u32,
    frames: usize,
    meta: HashMap<String, String>,
}

fn main() -> Result<(), Box<dyn std::error::Error>> {
    let root = std::env::var("MP5_BENCH_ROOT")
        .map(PathBuf::from)
        .unwrap_or_else(|_| PathBuf::from(r"C:\Users\colli\OneDrive\Desktop"));
    let flac = root.join("- ORIGAMI!.flac");
    if !flac.exists() {
        eprintln!("Missing source: {}", flac.display());
        std::process::exit(1);
    }

    let out_repo = PathBuf::from("benchmarks/real-music");
    fs::create_dir_all(&out_repo)?;
    let out_mp5 = out_repo.join("ORIGAMI_mp5l_v3_alpha.mp5");
    let out_desk = root.join("ORIGAMI_mp5l_v3_alpha.mp5");
    let report_path = out_repo.join("ORIGAMI_ALPHA_SMOKE.md");

    eprintln!("Loading {} …", flac.display());
    let pcm = load_flac(&flac)?;
    let pcm_bytes = (pcm.samples.len() * 2) as u64;
    let duration = pcm.frames as f64 / pcm.sample_rate as f64;

    eprintln!("Encoding MP5-L v3 …");
    let t0 = Instant::now();
    let bitstream = mp5l::encode(&pcm.samples, pcm.channels);
    let enc_ms = t0.elapsed().as_secs_f64() * 1000.0;
    if bitstream.len() < 2 || bitstream[0] != 0x4c || bitstream[1] != 3 {
        return Err(format!(
            "expected MP5-L v3 bitstream (0x4c 0x03), got {:02x} {:02x}",
            bitstream.first().copied().unwrap_or(0),
            bitstream.get(1).copied().unwrap_or(0)
        )
        .into());
    }

    let title = pcm
        .meta
        .get("title")
        .cloned()
        .unwrap_or_else(|| "ORIGAMI".into());
    let encoder = "MP5-L WASM v3 (lossless · bit-exact)";
    let mp5 = write_mp5(
        &bitstream,
        pcm.channels,
        pcm.sample_rate,
        pcm.frames,
        2,
        0,
        encoder,
        &title,
        &pcm.meta,
    )?;

    fs::write(&out_mp5, &mp5)?;
    fs::write(&out_desk, &mp5)?;
    eprintln!("Wrote {}", out_mp5.display());
    eprintln!("Wrote {}", out_desk.display());

    let t1 = Instant::now();
    let decoded = mp5l::decode(&bitstream)?;
    let dec_ms = t1.elapsed().as_secs_f64() * 1000.0;

    let n = pcm.samples.len().min(decoded.len());
    let mut max_diff = 0i32;
    for i in 0..n {
        max_diff = max_diff.max((pcm.samples[i] as i32 - decoded[i] as i32).abs());
    }
    let bit_exact = max_diff == 0 && decoded.len() == pcm.samples.len();
    let ratio = mp5.len() as f64 / pcm_bytes as f64;

    let meta_lines: String = if pcm.meta.is_empty() {
        "None (FLAC tags not found in container)".into()
    } else {
        pcm.meta
            .iter()
            .map(|(k, v)| format!("- **{k}:** {v}"))
            .collect::<Vec<_>>()
            .join("\n")
    };

    let md = format!(
        r#"# ORIGAMI MP5-L v3 Alpha smoke

## Source
- Path: `{}`
- Duration: {:.1}s
- Sample rate: {} Hz
- Channels: {}

## Export
- Path (repo): `{}`
- Path (Desktop): `{}`
- File size: {} bytes
- PCM reference: {} bytes
- Ratio vs PCM: {:.3}x ({:.1}% of PCM)
- Encode time: {:.0} ms
- Decode time: {:.0} ms

## Validation
- Bit-exact decode: **{}** (max sample diff: {})
- Hiss from codec: **{}** (lossless round-trip)
- MP5-L bitstream version: v{}

## Metadata (from FLAC → META chunk)
{}

## Format panel (expected in player)
- Codec label: MP5-L (lossless · recommended)
- Encoder: {}
- Encoder version: v3 (LPC + delta + varint)
- Output quality: Bit-exact lossless
- Decode path: MP5-L WASM v3 decode (lossless)

## Playback
Run `pnpm dev` → Player → open `{}` or Desktop copy.

---
*Generated by `alpha_origami_smoke`*
"#,
        flac.display(),
        duration,
        pcm.sample_rate,
        pcm.channels,
        out_mp5.display(),
        out_desk.display(),
        mp5.len(),
        pcm_bytes,
        ratio,
        ratio * 100.0,
        enc_ms,
        dec_ms,
        if bit_exact { "yes" } else { "NO" },
        max_diff,
        if bit_exact { "none expected" } else { "possible — investigate" },
        bitstream[1],
        meta_lines,
        encoder,
        out_desk.display(),
    );

    fs::write(&report_path, &md)?;
    eprintln!("\n{}", md);

    if !bit_exact {
        std::process::exit(1);
    }
    Ok(())
}

fn load_flac(path: &Path) -> Result<Pcm, Box<dyn std::error::Error>> {
    use symphonia::core::audio::SampleBuffer;
    use symphonia::core::codecs::DecoderOptions;
    use symphonia::core::errors::Error;
    use symphonia::core::formats::FormatOptions;
    use symphonia::core::io::MediaSourceStream;
    use symphonia::core::meta::MetadataOptions;
    use symphonia::core::probe::Hint;

    let src = fs::File::open(path)?;
    let mss = MediaSourceStream::new(Box::new(src), Default::default());
    let mut hint = Hint::new();
    if let Some(ext) = path.extension().and_then(|e| e.to_str()) {
        hint.with_extension(ext);
    }
    let probed = symphonia::default::get_probe().format(
        &hint,
        mss,
        &FormatOptions::default(),
        &MetadataOptions::default(),
    )?;

    let mut meta = HashMap::new();
    if let Some(stem) = path.file_stem().and_then(|s| s.to_str()) {
        meta.insert("title".to_string(), stem.replace('-', " ").trim().to_string());
    }

    let mut format = probed.format;
    let track = format
        .tracks()
        .iter()
        .find(|t| t.codec_params.codec != symphonia::core::codecs::CODEC_TYPE_NULL)
        .ok_or("no track")?
        .clone();
    let track_id = track.id;
    let mut decoder =
        symphonia::default::get_codecs().make(&track.codec_params, &DecoderOptions::default())?;
    let sample_rate = track.codec_params.sample_rate.unwrap_or(48000);
    let channels = track.codec_params.channels.map(|c| c.count()).unwrap_or(2) as u8;
    let mut planes: Vec<Vec<i16>> = (0..channels).map(|_| Vec::new()).collect();
    while let Ok(packet) = format.next_packet() {
        if packet.track_id() != track_id {
            continue;
        }
        match decoder.decode(&packet) {
            Ok(decoded) => {
                let spec = *decoded.spec();
                let mut buf = SampleBuffer::<i16>::new(decoded.capacity() as u64, spec);
                buf.copy_interleaved_ref(decoded);
                let ch = spec.channels.count();
                for (i, &s) in buf.samples().iter().enumerate() {
                    planes[i % ch].push(s);
                }
            }
            Err(Error::IoError(_)) => break,
            Err(Error::DecodeError(_)) => continue,
            Err(_) => break,
        }
    }
    let frames = planes[0].len();
    let mut interleaved = Vec::with_capacity(frames * channels as usize);
    for i in 0..frames {
        for c in 0..channels as usize {
            interleaved.push(planes[c][i]);
        }
    }
    Ok(Pcm {
        samples: interleaved,
        channels,
        sample_rate,
        frames,
        meta,
    })
}

fn write_mp5(
    audi: &[u8],
    channels: u8,
    sample_rate: u32,
    frames_per_ch: usize,
    codec_id: u8,
    preset_id: u8,
    encoder: &str,
    title: &str,
    meta: &HashMap<String, String>,
) -> Result<Vec<u8>, Box<dyn std::error::Error>> {
    let mut head = vec![0u8; 32];
    head[0] = codec_id;
    head[1] = channels;
    head[2] = 16;
    head[3] = preset_id;
    head[4..8].copy_from_slice(&sample_rate.to_le_bytes());
    head[8..16].copy_from_slice(&(frames_per_ch as u64).to_le_bytes());
    head[16..18].copy_from_slice(&1u16.to_le_bytes());

    let mut audi_wr = Vec::with_capacity(10 + audi.len());
    audi_wr.extend_from_slice(&0u32.to_le_bytes());
    audi_wr.extend_from_slice(&(audi.len() as u32).to_le_bytes());
    audi_wr.push(0);
    audi_wr.push(0);
    audi_wr.extend_from_slice(audi);

    let mut entries: Vec<(&str, &str)> = meta
        .iter()
        .map(|(k, v)| (k.as_str(), v.as_str()))
        .collect();
    if !entries.iter().any(|(k, _)| *k == "title") {
        entries.push(("title", title));
    }
    let meta_blob = encode_meta_fields(&entries);
    let info_blob = encode_meta_fields(&[("encoder", encoder)]);

    let wave = {
        let mut w = vec![0u8; 16];
        w[0..4].copy_from_slice(&3u32.to_le_bytes());
        w[4..8].copy_from_slice(&0.1f32.to_le_bytes());
        w[8..12].copy_from_slice(&0.5f32.to_le_bytes());
        w[12..16].copy_from_slice(&0.9f32.to_le_bytes());
        w
    };
    let seek = [0u8; 16];

    let mut parts: Vec<Vec<u8>> = Vec::new();
    for (fourcc, payload) in [
        ("HEAD", head.as_slice()),
        ("META", meta_blob.as_slice()),
        ("AUDI", audi_wr.as_slice()),
        ("SEEK", seek.as_slice()),
        ("WAVE", wave.as_slice()),
        ("INFO", info_blob.as_slice()),
    ] {
        let mut header = vec![0u8; 16];
        for (i, b) in fourcc.bytes().enumerate() {
            header[i] = b;
        }
        header[4..8].copy_from_slice(&(payload.len() as u32).to_le_bytes());
        header[8..10].copy_from_slice(&1u16.to_le_bytes());
        header[12..16].copy_from_slice(&crc32fast::hash(payload).to_le_bytes());
        let mut chunk = header;
        chunk.extend_from_slice(payload);
        parts.push(chunk);
    }
    let mut out = Vec::new();
    out.extend_from_slice(b"MP5A");
    out.extend_from_slice(&1u32.to_le_bytes());
    out.extend_from_slice(&0u32.to_le_bytes());
    for p in parts {
        out.extend_from_slice(&p);
    }
    Ok(out)
}

/// Matches `packages/mp5-container` `encodeMeta` (u16 key len, u16 value len).
fn encode_meta_fields(entries: &[(&str, &str)]) -> Vec<u8> {
    let mut out = Vec::new();
    for (key, val) in entries {
        let k = key.as_bytes();
        let v = val.as_bytes();
        out.extend_from_slice(&(k.len() as u16).to_le_bytes());
        out.extend_from_slice(k);
        out.extend_from_slice(&(v.len() as u16).to_le_bytes());
        out.extend_from_slice(v);
    }
    out
}
