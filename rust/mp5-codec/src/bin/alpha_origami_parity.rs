    let mp5_repo = PathBuf::from("benchmarks/real-music/ORIGAMI_mp5l_v3_alpha.mp5");
    let mp5_path = if mp5_desk.exists() {
        mp5_desk
    } else if mp5_repo.exists() {
        mp5_repo
    } else {
        eprintln!("Missing MP5 export — run alpha_origami_smoke first");
        std::process::exit(1);
    };
    let report = PathBuf::from("benchmarks/real-music/ORIGAMI_L_PCM_PARITY.md");

    if !flac.exists() {
        eprintln!("Missing {}", flac.display());
        std::process::exit(1);
    }

    eprintln!("Loading FLAC …");
    let flac_pcm = load_flac(&flac)?;

