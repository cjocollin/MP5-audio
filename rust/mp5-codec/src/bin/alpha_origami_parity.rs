fn null_test(a: &[i16], b: &[i16]) -> NullTest {
    let n = a.len().min(b.len());
    let mut max_diff = 0i32;
    let mut sum_sq = 0f64;
    let mut non_zero = 0usize;
    let mut peak_a = 0i16;
    let mut peak_b = 0i16;
    let mut sum_a = 0f64;
    let mut sum_b = 0f64;
    for i in 0..n {
        let da = a[i] as i32;
        let db = b[i] as i32;
        let d = (da - db).abs();
        max_diff = max_diff.max(d);
        if d != 0 {
            non_zero += 1;
        }
        sum_sq += (da - db) as f64 * (da - db) as f64;
        peak_a = peak_a.max(a[i].abs());
        peak_b = peak_b.max(b[i].abs());
        sum_a += (da * da) as f64;
        sum_b += (db * db) as f64;
    }
    let rms_diff = if n > 0 {
        (sum_sq / n as f64).sqrt()
    } else {
        0.0
    };
    let rms_a = if n > 0 {
        (sum_a / n as f64).sqrt()
    } else {
        0.0
    };
    let rms_b = if n > 0 {
        (sum_b / n as f64).sqrt()
    } else {
        0.0
    };
    NullTest {
        max_diff,
        rms_diff,
        non_zero_samples: non_zero,
        peak_a,
        peak_b,
        rms_a,
        rms_b,
    }
}

fn parse_head_from_mp5(data: &[u8]) -> Result<(u8, u8, u32, usize), String> {
    let payload = find_chunk_payload(data, b"HEAD").ok_or("HEAD chunk missing")?;
    if payload.len() < 16 {
        return Err("HEAD too short".into());
    }
    let codec = payload[0];
    let ch = payload[1];
    let rate = u32::from_le_bytes(payload[4..8].try_into().unwrap());
    let frames = u64::from_le_bytes(payload[8..16].try_into().unwrap()) as usize;
    Ok((codec, ch, rate, frames))
}

fn extract_audi_bitstream(data: &[u8]) -> Result<Vec<u8>, String> {
    let payload = find_chunk_payload(data, b"AUDI").ok_or("AUDI chunk missing")?;
    if payload.len() < 10 {
        return Err("AUDI too short".into());
    }
    let enc_len = u32::from_le_bytes(payload[4..8].try_into().unwrap()) as usize;
    if payload.len() < 10 + enc_len {
        return Err("AUDI payload truncated".into());
    }
    Ok(payload[10..10 + enc_len].to_vec())
}

fn find_chunk_payload<'a>(data: &'a [u8], fourcc: &[u8; 4]) -> Option<&'a [u8]> {
    if data.len() < 12 || &data[0..4] != b"MP5A" {
        return None;
    }
    let mut pos = 12usize;
    while pos + 16 <= data.len() {
        let id = &data[pos..pos + 4];
        let len = u32::from_le_bytes(data[pos + 4..pos + 8].try_into().ok()?) as usize;
        let start = pos + 16;
        if start + len > data.len() {
            break;
        }
        if id == fourcc {
            return Some(&data[start..start + len]);
        }
        pos = start + len;
    }
    None
}

fn load_flac(path: &std::path::Path) -> Result<Pcm, Box<dyn std::error::Error>> {
    use symphonia::core::audio::SampleBuffer;
    use symphonia::core::codecs::DecoderOptions;
    use symphonia::core::errors::Error;
    use symphonia::core::formats::FormatOptions;
    use symphonia::core::io::MediaSourceStream;
    use symphonia::core::meta::MetadataOptions;
    use symphonia::core::probe::Hint;

    let src = fs::File::open(path)?;
    let mss = MediaSourceStream::new(Box::new(src), Default::default());
    let mut hint = Hint::new();
    if let Some(ext) = path.extension().and_then(|e| e.to_str()) {
        hint.with_extension(ext);
    }
    let probed = symphonia::default::get_probe().format(
        &hint,
        mss,
        &FormatOptions::default(),
        &MetadataOptions::default(),
    )?;
    let mut format = probed.format;
    let track = format
        .tracks()
        .iter()
        .find(|t| t.codec_params.codec != symphonia::core::codecs::CODEC_TYPE_NULL)
        .ok_or("no track")?
        .clone();
    let track_id = track.id;
    let mut decoder =
        symphonia::default::get_codecs().make(&track.codec_params, &DecoderOptions::default())?;
    let sample_rate = track.codec_params.sample_rate.unwrap_or(48000);
    let channels = track.codec_params.channels.map(|c| c.count()).unwrap_or(2) as u8;
    let mut planes: Vec<Vec<i16>> = (0..channels).map(|_| Vec::new()).collect();
    while let Ok(packet) = format.next_packet() {
        if packet.track_id() != track_id {
            continue;
        }
        match decoder.decode(&packet) {
            Ok(decoded) => {
                let spec = *decoded.spec();
                let mut buf = SampleBuffer::<i16>::new(decoded.capacity() as u64, spec);
                buf.copy_interleaved_ref(decoded);
                let ch = spec.channels.count();
                for (i, &s) in buf.samples().iter().enumerate() {
                    planes[i % ch].push(s);
                }
            }
            Err(Error::IoError(_)) => break,
            Err(Error::DecodeError(_)) => continue,
            Err(_) => break,
        }
    }
    let frames = planes[0].len();
    let mut interleaved = Vec::with_capacity(frames * channels as usize);
    for i in 0..frames {
        for c in 0..channels as usize {
            interleaved.push(planes[c][i]);
        }
    }
    Ok(Pcm {
        samples: interleaved,
        channels,
        sample_rate,
        frames,
    })
}
