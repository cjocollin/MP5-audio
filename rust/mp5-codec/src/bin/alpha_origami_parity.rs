//! MP5-L vs PCM parity: FLAC source vs MP5-L v3 decode (null test).
//!   cargo run --release -p mp5-codec --features bench_tools --bin alpha_origami_parity

use mp5_codec::mp5l;
use std::fs;
use std::path::PathBuf;

struct Pcm {
    samples: Vec<i16>,
    channels: u8,
    sample_rate: u32,
    frames: usize,
}

struct NullTest {
    max_diff: i32,
    rms_diff: f64,
    non_zero_samples: usize,
    peak_a: i16,
    peak_b: i16,
    rms_a: f64,
    rms_b: f64,
}

